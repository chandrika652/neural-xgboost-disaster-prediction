# Cell 5: Preprocessing pipeline creation
# Why: Use sklearn ColumnTransformer to ensure reproducible preprocessing for numeric and categorical data.

from sklearn.impute import SimpleImputer

numeric_transformer = Pipeline(steps=[
    ('imputer', SimpleImputer(strategy='median')),
    ('scaler', StandardScaler())
])

