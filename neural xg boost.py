# Cell 8: Neural Network feature extractor
# Why: Train a neural net to learn high-level representations; extract the bottleneck layer outputs to feed XGBoost.

input_dim = X_train_bal.shape[1]
embedding_dim = min(64, max(16, input_dim // 4))  # heuristic

def build_feature_extractor(input_dim, embedding_dim=32):
    inp = keras.Input(shape=(input_dim,))
    x = layers.Dense(256, activation='relu')(inp)
    x = layers.BatchNormalization()(x)
    x = layers.Dropout(0.2)(x)
    x = layers.Dense(128, activation='relu')(x)
    x = layers.BatchNormalization()(x)
    bottleneck = layers.Dense(embedding_dim, activation='relu', name='bottleneck')(x)
    out = layers.Dense(len(le.classes_), activation='softmax')(bottleneck)
    model = keras.Model(inputs=inp, outputs=out)
    model.compile(optimizer='adam', loss='sparse_categorical_crossentropy', metrics=['accuracy'])
    return model

fe_model = build_feature_extractor(input_dim, embedding_dim=embedding_dim)
