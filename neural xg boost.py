# 3️⃣ Compute macro-average ROC curve
def compute_macro_roc(y_true_bin, y_prob, n_classes):
    fpr = dict(); tpr = dict(); roc_auc = dict()
    for i in range(n_classes):
        fpr[i], tpr[i], _ = roc_curve(y_true_bin[:, i], y_prob[:, i])
        roc_auc[i] = auc(fpr[i], tpr[i])
    all_fpr = np.unique(np.concatenate([fpr[i] for i in range(n_classes)]))
    mean_tpr = np.zeros_like(all_fpr)
    for i in range(n_classes):
        mean_tpr += np.interp(all_fpr, fpr[i], tpr[i])
    mean_tpr /= n_classes
    roc_auc["macro"] = auc(all_fpr, mean_tpr)
    return all_fpr, mean_tpr, roc_auc["macro"]

fpr_nn, tpr_nn, auc_nn = compute_macro_roc(y_test_bin, y_prob_nn, n_classes)
fpr_xgb, tpr_xgb, auc_xgb = compute_macro_roc(y_test_bin, y_prob_xgb, n_classes)
fpr_hy,  tpr_hy,  auc_hy  = compute_macro_roc(y_test_bin, y_prob_hybrid, n_classes)

